    public static void task21(String[] args) {
        int[] arr = {2, 9, -16, 25, -36, 49, -64, 81, -100, 121, -144};
        
        // Знайдемо максимальний і мінімальний елементи
        int max = arr[0];
        int min = arr[0];
        
        for (int i = 1; i < arr.length; i++) {
            if (arr[i] > max) {
                max = arr[i];
            }
            if (arr[i] < min) {
                min = arr[i];
            }
        }
        
        // Обчислимо модулі
        int absMax = Math.abs(max);
        int absMin = Math.abs(min);
        
        // Знайдемо добуток
        int product = absMax * absMin;
        
        // Виведемо результат
        System.out.println("Добуток модулів найбільшого та найменшого елементів: " + product);
    }
   
    public static void main(String[] args ) {
        int t = task21;
    }
